package parkinglot.enums;

/**
 * Represents the type of a vehicle entering the parking lot.
 * Vehicle type drives spot compatibility and fee rate selection.
 */
public enum VehicleType {
    CAR,        // Standard passenger car
    TRUCK,      // Large commercial or personal truck
    VAN,        // Passenger or cargo van
    MOTORCYCLE  // Two-wheeled motorized vehicle
}
