package parkinglot.enums;

/**
 * Represents the type of a parking spot.
 * Each type serves specific vehicle categories and requirements.
 */
public enum SpotType {
    ACCESSIBLE,   // Reserved for individuals with disabilities
    COMPACT,      // Suitable for smaller vehicles like cars
    LARGE,        // Suitable for trucks, vans, and larger vehicles
    MOTORCYCLE    // Dedicated spots for motorcycles
}
